const penjumlahan = 10 + 5;
console.log(penjumlahan); // 15

const pengurangan = 10 - 2;
console.log(pengurangan); // 8

const perkalian = 10 * 2;
console.log(perkalian); // 20

const pembagian = 10 / 2;
console.log(pembagian); // 5

const modulus = 10 % 3;
console.log(modulus); // 1 - karena 10 dibagi 3 sisanya 1

const eksponensial = 5 ** 3; // 5 pangkat 3
console.log(eksponensial); // 125

let increment = 5;
increment++; // 5 + 1
console.log(increment); // 6

let decrement = 5;
decrement--; // 5 - 1
console.log(decrement); // 4