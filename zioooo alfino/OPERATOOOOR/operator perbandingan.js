// ==
const a = 10;
const b = '10';
console.log(a == b); // true

// ===
const c = 10;
const d = '10';
console.log(a === d); // false

// !=
const e = 10;
const f = '10';
console.log(e != f); // false

// !==
const g = 10;
const h = '10';
console.log(g !== h); // true

// >
const i = 3;
const j = 3;
console.log(i > j); // false

// <>
const k = 3;
const l = 5;
console.log(k < l); // true

// >=
const m = 3;
const n = 3;
console.log(m >= n); // true

// <=
const o = 3;
const p = 5;
console.log(o <= p); // true